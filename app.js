function byStart(a, b) {
  return a.startsAt - b.startsAt;
}

function normalizeFixture(value) {
  return value
    .toLowerCase()
    .replaceAll("south korea", "korea")
    .replaceAll("korea republic", "korea")
    .replaceAll("czech republic", "czech")
    .replaceAll("czechia", "czech")
    .replaceAll("republic", "")
    .replaceAll("bosnia and herzegovina", "bosnia-herzegovina")
    .replaceAll("bosnia & herzegovina", "bosnia-herzegovina")
    .replaceAll("cote d'ivoire", "ivory coast")
    .replaceAll("dr congo", "congo dr")
    .replaceAll("cabo verde", "cape verde")
    .replace(/\bv\b/g, "vs")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();
}

function normalizeTeamName(value) {
  return normalizeFixture(value)
    .replace(/\bfc\b/g, "")
    .replace(/\bafc\b/g, "")
    .replace(/\bthe\b/g, "")
    .replace(/\s+/g, " ")
    .trim();
}

function getFixtureTeams(fixture) {
  const teams = fixture.split(/\s+v(?:s)?\s+/i);
  return {
    home: teams[0] ?? fixture,
    away: teams[1] ?? "",
  };
}

function fixtureKey(home, away) {
  return `${normalizeTeamName(home)}::${normalizeTeamName(away)}`;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;");
}

function isFreeMatch(match) {
  const normalized = normalizeFixture(match.fixture ?? match.match);
  const startTime = match.startsAt?.getTime();
  return freeMatches.some((freeMatch) => (
    normalizeFixture(freeMatch.match) === normalized ||
    (startTime && freeMatch.startsAt.getTime() === startTime)
  ));
}

function getUpcoming(items, now = new Date()) {
  return items.filter((item) => item.startsAt > now).sort(byStart);
}

function matchStatusLabel(status) {
  const labels = {
    SCHEDULED: "Scheduled",
    TIMED: "Timed",
    IN_PLAY: "Live",
    LIVE: "Live",
    PAUSED: "Half-time",
    FINISHED: "Final",
    POSTPONED: "Postponed",
    SUSPENDED: "Suspended",
    CANCELLED: "Cancelled",
  };
  return labels[status] ?? status ?? "Scheduled";
}

function isLiveApiStatus(status) {
  return ["IN_PLAY", "LIVE", "PAUSED"].includes(status);
}

function scoreText(match) {
  if (Number.isFinite(match?.homeScore) && Number.isFinite(match?.awayScore)) {
    return `${match.homeScore} - ${match.awayScore}`;
  }
  return isLiveApiStatus(match?.status) ? "LIVE" : "--";
}

function getApiMatchForFixture(fixture) {
  const teams = getFixtureTeams(fixture);
  const key = fixtureKey(teams.home, teams.away);
  const reverseKey = fixtureKey(teams.away, teams.home);
  return liveScoreFeed.matches.find((match) => {
    const apiKey = fixtureKey(match.homeTeam, match.awayTeam);
    return apiKey === key || apiKey === reverseKey;
  }) ?? null;
}

function getApiFocusMatch() {
  return liveScoreFeed.matches.find((match) => isLiveApiStatus(match.status)) ?? null;
}

function getScheduleMatchForApi(apiMatch) {
  const apiKey = fixtureKey(apiMatch.homeTeam, apiMatch.awayTeam);
  const reverseKey = fixtureKey(apiMatch.awayTeam, apiMatch.homeTeam);
  return fullSchedule.find((match) => {
    const teams = getFixtureTeams(match.fixture);
    const scheduleKey = fixtureKey(teams.home, teams.away);
    return scheduleKey === apiKey || scheduleKey === reverseKey;
  }) ?? null;
}

function formatDuration(ms) {
  if (ms <= 0) return "00:00:00";
  const totalSeconds = Math.floor(ms / 1000);
  const days = Math.floor(totalSeconds / 86400);
  const hours = Math.floor((totalSeconds % 86400) / 3600);
  const minutes = Math.floor((totalSeconds % 3600) / 60);
  const seconds = totalSeconds % 60;
  const time = [hours, minutes, seconds].map((value) => String(value).padStart(2, "0")).join(":");
  return days > 0 ? `${days}d ${time}` : time;
}

function getMatchState(now = new Date()) {
  const matchWindowMs = 2 * 60 * 60 * 1000;
  const live = fullSchedule.find((match) => now >= match.startsAt && now < new Date(match.startsAt.getTime() + matchWindowMs));
  const upcoming = getUpcoming(fullSchedule, now);
  const next = upcoming[0] ?? null;
  const last = fullSchedule.filter((match) => match.startsAt <= now).sort(byStart).at(-1) ?? null;
  return { live, next, last };
}

